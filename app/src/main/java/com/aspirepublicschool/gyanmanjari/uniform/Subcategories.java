package com.aspirepublicschool.gyanmanjari.uniform;

public class Subcategories {
    String sub;
    int img;

    public Subcategories(String sub, int img) {
        this.sub = sub;
        this.img = img;
    }

    public String getSub() {
        return sub;
    }

    public void setSub(String sub) {
        this.sub = sub;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
